package main

import (
	"fmt"
)

func main() {
	fmt.Printf("Type: %T - Value: %v\n", true, true)
	fmt.Printf("Type: %T - Value: %v\n", "steph", "dornas")
	fmt.Printf("Type: %T - Value: %v\n", 1, 17268)
	fmt.Printf("Type: %T - Value: %v\n", "1", "1627")
	fmt.Printf("Type: %T - Value: %v\n", 1.233, 1.557)
}
